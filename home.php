<h2>Description</h2>
<p>We wanted to make this site very modern and sleek while sticking to the UVM cycling team style. The fonts and colors are the ones used by the team. The big sweeping photos and general layout of the site is in tune with other professional cycling team sites. There were no major problems or issues with the website mentioned by the usability testers or classmates when we presented, because we designed and developed the site with the user in mind.The only thing that was mentioned was the navigation font was hard to read so we spaced out the letters. The sight is very simple to use and the navigatation links are named to perfectly describe what the page is for. Each page has the title so that the user always knows where they are.</p>

